package md.tekwillacademy.variablesandcomments;

public class CommentsAndVariables {
    public static void main(String[] args) {
        boolean isHotOutside;
        isHotOutside = true;
        System.out.println("The value of isHotOutside variable is: "+isHotOutside);
        boolean isFriday = true;
        System.out.println("The value of isFriday variable is: "+isFriday);
        char myFirstCharVariable = 'A';
        char mySecondCharVariable = 'Z';
        byte myFirstByteVariable = -128;
        byte mySecondByteVariable = 127;
        short myFirstShortVariable = -32768;
        short mySecondShortVariable = 32767;
        int myFirstIntVariable = -2147483648;
        int mySecondIntVariable = 2147483647;
        long myFirstLongVariable = -9223372036854775808L;
        long mySecondLongVariable = 9223372036854775807L;
        float myFirstFloatVariable = -3.4E+38f;
        float mySecondFloatVariable = 3.14E+38f;
        double myFirstDoubleVariable = -1.7E+308d;
        double mySecondDoubleVariable = -1.7E+308d;
        String myStringVariable = "Lilia";
        Object myObjectVariable = "Car";

    }


}
